# Usado para percorrer uma coleção de elementos.

fruits = ['Maçã', 'Uva', 'Morango']

for fruit in fruits
    puts fruit 
end

# No exemplo, a instrução for percorrerá todos os elementos da lista fruits. 
# Em cada iteração, podemos acessar o elemento atual através da variável fruit.

